    public void setRotateAngle(ModelRenderer modelRenderer, float x, float y, float z) {
        modelRenderer.rotateAngleX = x;
        modelRenderer.rotateAngleY = y;
        modelRenderer.rotateAngleZ = z;
    }
    
	/**
	 * Sets the model's various rotation angles. For bipeds, time and maxSwing are
	 * used for animating the movement of arms and legs, where time represents
	 * the time(so that arms and legs swing back and forth) and maxSwing represents
	 * how "far" arms and legs can swing at most.
	 */
    @Override
	public void setRotationAngles(float time, float maxSwing, float var0, float headAngleY, float headAngleX, float var1, Entity entity)
	{
		this.lArm00.rotateAngleX = -1.5F * this.calcSwing(time, 30.0F) * maxSwing;
        this.rArm00.rotateAngleX =  1.5F * this.calcSwing(time, 30.0F) * maxSwing;
	}

	private float calcSwing(float time, float swing)
	{
		return (Math.abs(time % swing - swing * 0.5F) - swing * 0.25F) / (swing * 0.50F);
	}
